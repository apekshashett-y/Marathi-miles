// Helper function to get all unique years
export const getTimelineYears = () => {
  const years = maharashtraHistoricalEvents.map(event => event.year);
  return years.sort((a, b) => a - b);
};

// Helper function to get events by year
export const getEventsByYear = (year) => {
  const yearData = maharashtraHistoricalEvents.find(event => event.year === year);
  return yearData ? yearData.events : [];
};

// Get earliest and latest year for timeline range
export const getTimelineRange = () => {
  const years = getTimelineYears();
  return {
    start: Math.min(...years),
    end: Math.max(...years)
  };
};

// Search function for events
export const searchEvents = (query) => {
  const searchTerm = query.toLowerCase();
  
  const results = maharashtraHistoricalEvents.flatMap(yearData => 
    yearData.events
      .filter(event => 
        event.title.toLowerCase().includes(searchTerm) ||
        event.fullDescription.toLowerCase().includes(searchTerm) ||
        event.tags.some(tag => tag.toLowerCase().includes(searchTerm)) ||
        event.category.toLowerCase().includes(searchTerm) ||
        yearData.year.toString().includes(searchTerm) ||
        event.location.toLowerCase().includes(searchTerm) ||
        yearData.era.toLowerCase().includes(searchTerm)
      )
      .map(event => ({
        ...event,
        year: yearData.year,
        era: yearData.era
      }))
  );
  
  return results;
};

// Get era by year
export const getEraByYear = (year) => {
  const yearData = maharashtraHistoricalEvents.find(event => event.year === year);
  return yearData ? yearData.era : "Unknown Era";
};

// Get events by category
export const getEventsByCategory = (category) => {
  return maharashtraHistoricalEvents.flatMap(yearData => 
    yearData.events
      .filter(event => event.category.toLowerCase() === category.toLowerCase())
      .map(event => ({
        ...event,
        year: yearData.year,
        era: yearData.era
      }))
  );
};

// Get events by location
export const getEventsByLocation = (location) => {
  return maharashtraHistoricalEvents.flatMap(yearData => 
    yearData.events
      .filter(event => event.location.toLowerCase().includes(location.toLowerCase()))
      .map(event => ({
        ...event,
        year: yearData.year,
        era: yearData.era
      }))
  );
};

// Get statistics
export const getTimelineStatistics = () => {
  const totalEvents = maharashtraHistoricalEvents.reduce((sum, year) => sum + year.events.length, 0);
  const eras = [...new Set(maharashtraHistoricalEvents.map(event => event.era))];
  const categories = [...new Set(maharashtraHistoricalEvents.flatMap(year => year.events.map(event => event.category)))];
  
  return {
    totalYears: maharashtraHistoricalEvents.length,
    totalEvents: totalEvents,
    eras: eras,
    categories: categories,
    timelineSpan: `${maharashtraHistoricalEvents[0].year} - ${maharashtraHistoricalEvents[maharashtraHistoricalEvents.length - 1].year}`
  };
};